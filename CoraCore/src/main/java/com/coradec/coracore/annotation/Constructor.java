package com.coradec.coracore.annotation;

import static java.lang.annotation.ElementType.*;

import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a static factory method as THE constructor to be used for the annotated implementation
 * class.
 * <p>
 * In the presence of an annotated public static factory method that does its job, constructors will
 * be ignored.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({METHOD, CONSTRUCTOR})
public @interface Constructor {

}
